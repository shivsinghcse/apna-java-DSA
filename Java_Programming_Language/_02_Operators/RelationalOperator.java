package Java_Programming_Language._02_Operators;

public class RelationalOperator {
    public static void main(String[] args) {
        
        int a = 10;
        int b = 5;

        System.out.println(a == b); // false
        System.out.println(a != b); // true
        System.out.println(a > b); // true
        System.out.println(a < b); // false
        System.out.println(a >= b); // true
        System.out.println(a <= b); // false
    }
}
